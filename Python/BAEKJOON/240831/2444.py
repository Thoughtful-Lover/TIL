'''
2444. 별 찍기 - 7

첫째 줄에 N(1<=N<=100)

예제 입력
5

예제 출력
    *
   ***
  *****
 *******
*********
 *******
  *****
   ***
    *
'''

N = int(input())
for i in range(1, 2*N):
    if i<=N:
        print(*([' ']*(2*(N-i)//2)+['*']*(2*i-1)+[' ']*(2*(N-i)//2)), sep='')
    else:
        print(*([' ']*((2*(N-i)+(i%N*4))//2)+['*']*((2*i-1)-(i%N*4))+[' ']*((2*(N-i)+(i%N*4))//2)), sep='')